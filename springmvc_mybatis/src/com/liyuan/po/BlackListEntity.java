package com.liyuan.po;

import java.util.Date;

public class BlackListEntity {
	
	String id;
	
	String yhid;
	
	Date lhsj;
	
	String repoid;
	
	String username;
	
	int lhyy;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getYhid() {
		return yhid;
	}

	public void setYhid(String yhid) {
		this.yhid = yhid;
	}

	public Date getLhsj() {
		return lhsj;
	}

	public void setLhsj(Date lhsj) {
		this.lhsj = lhsj;
	}

	public String getRepoid() {
		return repoid;
	}

	public void setRepoid(String repoid) {
		this.repoid = repoid;
	}

	public String getUsername() {
		return username;
	}

	public void setUsername(String username) {
		this.username = username;
	}

	public int getLhyy() {
		return lhyy;
	}

	public void setLhyy(int lhyy) {
		this.lhyy = lhyy;
	}
	
	
}
